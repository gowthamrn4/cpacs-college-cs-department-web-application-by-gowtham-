      <h1 class="title">Contents</h1>
       
       
       
       
       
       
       
       
       
       <p>C is a general-purpose high level language that was originally developed by Dennis Ritchie for the Unix operating system. It was first implemented on the Digital Eqquipment Corporation PDP-11 computer in 1972.</p>
<p>The Unix operating system and virtually all Unix applications are written in the C language. C has now become a widely used professional language for various reasons.</p>
<ul>
<li><p>Easy to learn</p></li>
<li><p>Structured language</p></li>
<li><p>It produces efficient programs.</p></li>
<li><p>It can handle low-level activities.</p></li>
<li><p>It can be compiled on a variety of computers.</p></li>
</ul>
<h2>Facts about C</h2>
<ul>
<li><p>C was invented to write an operating system called UNIX.</p></li>
<li><p>C is a successor of B language which was introduced around 1970</p></li>
<li><p>The language was formalized in 1988 by the American National Standard Institue (ANSI).</p></li>
<li><p>By 1973 UNIX OS almost totally written in C.</p></li>
<li><p>Today C is the most widely used System Programming Language.</p></li>
<li><p>Most of the state of the art software have been implemented using C</p></li>
</ul>
<h2>Why to use C ?</h2>
<p>C was initially used for system development work, in particular the programs that make-up the operating system. C was adoped as a system development language because it produces code that runs nearly as fast as code written in assembly language. Some examples of the use of C might be:</p>
<ul>
<li>     Operating Systems</li>
<li>     Language Compilers</li>
<li>     Assemblers</li>
<li>     Text Editors</li>
<li>     Print Spoolers</li>
<li>     Network Drivers</li>
<li>     Modern Programs</li>
<li>     Data Bases</li>
<li>     Language Interpreters</li>
<li>     Utilities</li>
</ul>
<h1>C Program File</h1>
<p>All the C programs are writen into text files with extension ".c" for example <b><i>hello.c</i></b>. You can use "vi" editor to write your C program into a file. </p>
<p>This tutorial assumes that you know how to edit a text file and how to write programming insturctions inside a program file.</p>
<h1>C Compilers</h1>
<p class="alignment">When you write any program in C language then to run that program you need to compile that program using a C Compiler which converts your program into a language understandable by a computer. This is called machine language (ie. binary format). So before proceeding, make sure you have C Compiler available at your computer. It comes alongwith all flavors of Unix and Linux. </p>
<p class="alignment">If you are working over Unix or Linux then you can type <i>gcc -v</i> or <i>cc  -v</i> and check the result. You can ask your system administrator or you can take help from anyone to identify an available C Compiler at your computer.</p>
<p>If you don't have C compiler installed at your computer then you can use below given link to download a GNU C Compiler and use it.</p>
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
