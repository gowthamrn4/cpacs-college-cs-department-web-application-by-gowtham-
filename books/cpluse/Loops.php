<p>There may be a situation, when you need to execute a block of code several number of times. In general, statements are executed sequentially: The first statement in a function is executed first, followed by the second, and so on.</p>
<p>Programming languages provide various control structures that allow for more complicated execution paths.</p>
<p>A loop statement allows us to execute a statement or group of statements multiple times and following is the general form of a loop statement in most of the programming languages:</p>
<img src="../images/loop_architecture.jpg" alt="Loop Architecture" />
<p>C programming language provides the following types of loop to handle looping requirements. Click the following links to check their detail.</p>
<table class="src">
<tr><th style="width:30%">Loop Type</th><th>Description</th></tr>
<tr><td><a href="c_while_loop.html" title="while loop in C">while loop</a></td><td>Repeats a statement or group of statements while a given condition is true. It tests the condition before executing the loop body.</td></tr>
<tr><td><a href="c_for_loop.html" title="for loop in C">for loop</a></td><td>Execute a sequence of statements multiple times and abbreviates the code that manages the loop variable.</td></tr>
<tr><td><a href="c_do_while_loop.html" title="do...while loop in C">do...while loop</a></td><td>Like a while statement, except that it tests the condition at the end of the loop body</td></tr>
<tr><td><a href="c_nested_loops.html" title="nested loops in C">nested loops</a></td><td>You can use one or more loop inside any another while, for or do..while loop.</td></tr>
</table>
<h2>Loop Control Statements:</h2>
<p>Loop control statements change execution from its normal sequence. When execution leaves a scope, all automatic objects that were created in that scope are destroyed.</p>
<p>C supports the following control statements. Click the following links to check their detail.</p>
<table class="src">
<tr><th style="width:30%">Control Statement</th><th>Description</th></tr>
<tr><td><a href="c_break_statement.html" title="break statement in C">break statement</a></td><td>Terminates the <b>loop</b> or <b>switch</b> statement and transfers execution to the statement immediately following the loop or switch.</td></tr>
<tr><td><a href="c_continue_statement.html" title="continue statement in C">continue statement</a></td><td>Causes the loop to skip the remainder of its body and immediately retest its condition prior to reiterating.</td></tr>
<tr><td><a href="c_goto_statement.html" title="goto statement in C">goto statement</a></td><td>Transfers control to the labeled statement. Though it is not advised to use goto statement in your program.</td></tr>
</table>
<h2>The Infinite Loop:</h2>
<p>A loop becomes infinite loop if a condition never becomes false. The <b>for</b> loop is traditionally used for this purpose. Since none of the three expressions that form the for loop are required, you can make an endless loop by leaving the conditional expression empty.</p>
<pre class="prettyprint">
#include &lt;stdio.h&gt;
 
int main ()
{

   for( ; ; )
   {
      printf("This loop will run forever.\n");
   }

   return 0;
}
</pre>
<p>When the conditional expression is absent, it is assumed to be true. You may have an initialization and increment expression, but C programmers more commonly use the for(;;) construct to signify an infinite loop.</p>
<p><b>NOTE:</b> You can terminate an infinite loop by pressing Ctrl + C keys.</p>

       
       
       
       
       
       
       
       
       
       
