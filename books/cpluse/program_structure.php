     <p>A C program basically has the following form:</p>
<ul>
<li><p>Preprocessor Commands</p></li>
<li><p>Functions</p></li>
<li><p>Variables</p></li>
<li><p>Statements &amp; Expressions</p></li>
<li><p>Comments</p></li>
</ul>
<p>The following program  is written in the C programming language. Open a text file <b><i>hello.c</i></b> using vi editor and put the following lines inside that file.</p>
<table cellpadding="10" class="src">
<tr>
<td><pre>
#include &lt;stdio.h&gt;

int main()
{
   /* My first program */
   printf("Hello, World! \n");
   
   return 0;
}
</pre>
</td></tr>
</table>
<p><b>Preprocessor Commands:</b> These commands tells the compiler to do preprocessing before doing actual compilation. Like <i>#include &lt;stdio.h&gt;</i> is a preprocessor command which tells a C compiler to include stdio.h file before going to actual compilation. You will learn more about C Preprocessors in <a href="/ansi_c/c_preprocessors.htm">C Preprocessors</a> session.</p>
<p><b>Functions: </b> are main building blocks of any C Program.  Every C Program will  have one or more functions and there is one mandatory function which is called <i>main()</i> function. This function is prefixed with keyword <i>int</i> which means this function returns an integer value when it exits. This integer value is retured using <i>return</i> statement.</p>
<p>The C Programming language provides a set of built-in functions. In the above example <i>printf()</i> is a C built-in function which is used to print anything on the screen. Check  <a href="/ansi_c/c_function_references.htm">Builtin  function</a> section for more detail.</p>
<p>You will learn how to write  your own functions and use them in <a href="/ansi_c/c_using_functions.htm">Using Function</a> session.</p>
<p><b>Variables:</b> are used to hold numbers, strings and complex data for manipulation. You will learn in detail about variables in <a href="/ansi_c/c_variable_types.htm">C Variable Types</a>.</p>
<p><b>Statements &amp; Expressions : </b> Expressions combine variables and constants to create new values. Statements are expressions, assignments, function calls, or control flow statements which make up C programs.</p>
<p><b>Comments:</b> are used to give additional useful information inside a C Program. All the comments will be put inside /*...*/ as given in the example above. A comment can span through multiple lines.</p>
<h2>Note the followings</h2>
<ul>
<li><p>C is a case sensitive programming language. It means in C <i>printf</i> and <i>Printf</i> will have different meanings.</p></li>
<li><p>C has a free-form line structure. End of each C statement must be marked with a semicolon.</p></li>
<li><p>Multiple statements can be one the same line. </p></li>
<li><p>White Spaces  (ie tab space and space bar ) are ignored.</p></li>
<li><p>Statements can continue over multiple lines.</p></li>
</ul>
<h1>C Program Compilation</h1>
<p>To compile a C program you would have to Compiler name and program files name. Assuming your compiler's name is cc and program file name is hello.c, give following command at Unix prompt.</p>
<table class="src">
<tr><td><pre>
$cc hello.c
</pre></td></tr>
</table>
<p>This will produce a binary file called <i>a.out</i> and an object file hello.o in your current directory. Here a.out is your first program which you will run at Unix prompt like any other system program. If you don't like the name <i>a.out</i> then you can produce a binary file with your own name by using -o option while compiling C program. See an example below</p>
<table class="src">
<tr><td><pre>
$cc -o hello hello.c
</pre></td></tr>
</table>
<p>Now you will get a binary with name <i>hello</i>. Execute this program at Unix prompt but before executing / running this program make sure that it has execute permission  set. If you don't know what is execute permission then just follow these two steps</p>
<table class="src">
<tr><td><pre>
$chmod 755 hello
$./hello

This will produce following result
Hello, World
</pre></td></tr>
</table>
<p>Congratulations!! you have written your first program in "C". Now believe me its not difficult to learn "C".</p>
</td>
